class shortcut_block(nn.Module):
    def __init__(self, channelIn, channelOut):
        super(shortcut_block, self).__init__()
        hinddenchannel=int(channelOut*0.5)
        self.CV1 = Conv(channelIn, hinddenchannel,1,1)

        self.conv = nn.Conv2d(hinddenchannel, channelOut, 3, 1, autopad(3, 1), bias=False)
        self.bn = nn.BatchNorm2d(channelOut)
        self.act = nn.ReLU()
        self.ResATT=ResATT()
    def forward(self, x):
        residual=x
        out=self.CV1(x)
        out=self.conv(out)
        out=self.bn(out)
        out +=residual
        out +=self.ResATT(x)
        out=self.act(out)
        return out
class att_block(nn.Module):
    def __init__(self, channelIn, channelOut):
        super(att_block, self).__init__()
        hinddenchannel=int(channelOut*0.5)
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        self.fc = nn.Sequential(
            nn.Conv2d(channelIn, hinddenchannel, kernel_size=1, stride=1, padding=0),
            nn.ReLU(inplace=True),
            nn.Conv2d(hinddenchannel, channelIn, kernel_size=1, stride=1, padding=0),
            nn.BatchNorm2d(channelIn)
        )
        self.fc1 = nn.Sequential(
            nn.Conv2d(channelIn, hinddenchannel, kernel_size=1, stride=1, padding=0),
            nn.ReLU(inplace=True),
            nn.Conv2d(hinddenchannel, channelIn, kernel_size=1, stride=1, padding=0),
            nn.Sigmoid()
        )
    def forward(self, x):
        out1=self.avg_pool(x).expand_as(x)
        out1=self.fc(out1)
        out2=self.fc(x)
        out=out1+out2
        out=self.fc1(out)
        x=x*out
        return x
class ResATT(nn.Module):
    def __init__(self):
        super(ResATT, self).__init__()
        self.conv1 = nn.Conv2d(2, 1,kernel_size=3, padding=1,bias=False)
        self.sigmoid = nn.Sigmoid()
    def forward(self,x):
        avg = torch.mean(x, dim=1, keepdim=True)
        max, _ = torch.max(x, dim=1, keepdim=True)
        x = torch.cat([avg, max], dim=1)
        x = self.conv1(x)
        x=self.sigmoid(x)
        return x
class feature_extraction_block(nn.Module):
    def __init__(self, channelIn, channelOut):
        super(feature_extraction_block, self).__init__()
        hinddenchannel=int(channelOut*0.5)
        self.CV1 = Conv(channelIn, hinddenchannel,1,1)

        self.conv = nn.Conv2d(hinddenchannel, channelOut, 3, 1, autopad(3, 1), bias=False)

        self.bn = nn.BatchNorm2d(channelOut)
        self.act = nn.SiLU()
        self.CL=ChannelAtt(channelOut)
        self.CDL=CoordAtt(channelIn, channelOut)
    def forward(self, x):
        residual=x
        out=self.CV1(x)
        out=self.conv(out)
        out=self.bn(out)
        out=self.CL(out)#CL
        out +=residual
        out=self.act(out)
        out = self.CDL(out)#CDL
        return out

class ChannelAtt(nn.Module):
    def __init__(self, in_planes, ratio=16):
        super(ChannelAtt, self).__init__()
        self.avg_pool= nn.AdaptiveAvgPool2d(1)
        self.max_pool= nn.AdaptiveMaxPool2d(1)

        self.fc1= nn.Conv2d(in_planes*2, in_planes//ratio, 1, bias=False)
        self.relu1= nn.ReLU()
        self.fc2= nn.Conv2d(in_planes//ratio, in_planes, 1, bias=False)
        self.sigmoid= nn.Sigmoid()

    def forward(self,x):
        avg=self.avg_pool(x)
        max=self.max_pool(x)
        concat=torch.cat([max,avg],dim=1)
        concat=self.relu1(self.fc1(concat))
        concat=self.fc2(concat)
        concat=self.sigmoid(concat)
        x=x*concat
        return x
class CoordAtt(nn.Module):
    def __init__(self, inp, oup, reduction=32):
        super(CoordAtt, self).__init__()
        self.pool_h = nn.AdaptiveAvgPool2d((None, 1))
        self.pool_w = nn.AdaptiveAvgPool2d((1, None))

        mip = max(8, inp // reduction)

        self.conv1 = nn.Conv2d(inp, mip, kernel_size=1, stride=1, padding=0)
        self.bn1 = nn.BatchNorm2d(mip)
        self.act = nn.ReLU()

        self.conv_h = nn.Conv2d(mip, oup, kernel_size=1, stride=1, padding=0)
        self.conv_w = nn.Conv2d(mip, oup, kernel_size=1, stride=1, padding=0)

    def forward(self, x):
        identity = x

        n, c, h, w = x.size()
        x_h = self.pool_h(x)
        x_w = self.pool_w(x).permute(0, 1, 3, 2)

        y = torch.cat([x_h, x_w], dim=2)
        y = self.conv1(y)
        y = self.bn1(y)
        y = self.act(y)

        x_h, x_w = torch.split(y, [h, w], dim=2)
        x_w = x_w.permute(0, 1, 3, 2)

        a_h = self.conv_h(x_h).sigmoid()
        a_w = self.conv_w(x_w).sigmoid()

        out = identity * a_w * a_h

        return out

